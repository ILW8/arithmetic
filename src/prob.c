int get_stage2_cost(int B1, int B2, int D, int A, int L, double pairing)
{
    int i, j;
    int stage2cost = 0;
    double num_primes = Ei(log(B2)) - Ei(log(B1));

    if (A > 1)
    {
        D /= A;
        L = L*A + 1;
    }

    if (primes == NULL)
        sieve(B2/B1 + 100);
    for (i = 0; primes[i]*B1 < B2; i++)
        if (D%primes[i] != 0)
        {
            B1 = B2/primes[i];
            break;
        }

    stage2cost++;
    stage2cost++;
    int dist = 1;
    //stage2cost += D/2*L/2;
    for (i = 1; i < D/2*L; i++)
    {
        if (gcd(2*i + 1, 2*dist) != 1)
            continue;
        stage2cost++;
        if ((i + 1)%dist == 0 && D%(i + 1) == 0 && gcd((i + 1)/dist, 2*dist) == 1)
        {
            stage2cost++;
            stage2cost++;
            dist = i + 1;
        }
    }

    if (A > 1)
    {
        if (D/2/dist != 1)
        {
            int pd = D/2/dist;
            for (j = 0; (pd & 1) == 0; pd >>= 1, j++);
            if (pd > 1)
                stage2cost++;
            stage2cost += j;
        }
        D *= A;
        L /= A;
    }
    if (D/2/dist != 1)
    {
        int pd = D/2/dist;
        for (j = 0; (pd & 1) == 0; pd >>= 1, j++);
        if (pd > 1)
            stage2cost++;
        stage2cost += j;
    }

    int v = B1/D;

    if (A > 1)
    {
        if (v > 0)
            stage2cost += 2*(int)(log(v*A)/log(2.0));
        stage2cost += A;
        stage2cost += L - 1;
    }
    else
    {
        if (v > 0)
            stage2cost += 2*(int)(log(v)/log(2.0));
    }

    stage2cost += L;
    stage2cost += (int)(num_primes - num_primes*pairing/2);
    stage2cost += (B2 - B1)/D;
    if (A > 1)
        stage2cost += (B2 - B1)/D;

    return stage2cost;
}

