int get_edecm_stage2_size(int D, int L, int LR)
{
    int i, j;
    int size = 0;

    size++;
    size++;
    size += 4;
    size += 4;
    size += 2;
    size += 2;
    char *precomp_V = malloc(D/2*L);
    memset(precomp_V, 0, D/2*L);

    size += 1;
    int dist = 1;
    for (i = 1; i < D/2*L; i++)
    {
        if (gcd(2*i + 1, 2*dist) != 1)
            continue;
        precomp_V[i] = 1;
        size += 1;
        if ((i + 1)%dist == 0 && D%(i + 1) == 0 && gcd((i + 1)/dist, 2*dist) == 1)
        {
            dist = i + 1;
            for (j = 0; j < i; j++)
                if (precomp_V[j] != 0 && gcd(2*j + 1, 2*dist) != 1)
                {
                    precomp_V[j] = 0;
                    size -= 1;
                }
        }
    }

    if (LR > 0)
        size += L + LR;
    else
        size += 2*L;

    free(precomp_V);
    return size;
}

int get_edecm_stage2_cost(int B1, int B2, int D, int L, int LR, double pairing)
{
    int i, j;
    int stage2cost = 0;
    double num_primes = Ei(log(B2)) - Ei(log(B1));

    if (primes == NULL)
        sieve(B2/B1 + 100);
    for (i = 0; primes[i]*B1 < B2; i++)
        if (D%primes[i] != 0)
        {
            B1 = B2/primes[i];
            break;
        }

    stage2cost++;
    stage2cost++;
    int dist = 1;
    //stage2cost += D/2*L/2;
    for (i = 1; i < D/2*L; i++)
    {
        if (gcd(2*i + 1, 2*dist) != 1)
            continue;
        stage2cost++;
        if ((i + 1)%dist == 0 && D%(i + 1) == 0 && gcd((i + 1)/dist, 2*dist) == 1)
        {
            stage2cost++;
            stage2cost++;
            dist = i + 1;
        }
    }

    if (D/2/dist != 1)
    {
        int pd = D/2/dist;
        for (j = 0; (pd & 1) == 0; pd >>= 1, j++);
        if (pd > 1)
            stage2cost++;
        stage2cost += j;
    }

    int v = B1/D;

    if (v > 0)
        stage2cost += 2*(int)(log(v)/log(2.0));

    stage2cost += L;
    stage2cost += (int)(num_primes - num_primes*pairing/2);
    stage2cost += (B2 - B1)/D;

    return stage2cost;
}

